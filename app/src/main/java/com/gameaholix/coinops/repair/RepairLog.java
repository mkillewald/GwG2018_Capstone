package com.gameaholix.coinops.repair;

import android.os.Parcel;
import android.os.Parcelable;

import com.google.firebase.database.Exclude;

public class RepairLog implements Parcelable{
    private String id;
    private String gameId;
    private String description;
    private String repairSteps;
    private Long timestamp;


    public RepairLog() {
        // Default constructor required for calls to DataSnapshot.getValue()
    }

    private RepairLog(Parcel in) {
        this.id = in.readString();
        this.gameId = in.readString();
        this.description = in.readString();
        this.repairSteps = in.readString();
        this.timestamp = in.readLong();
    }

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int i) {
        dest.writeString(id);
        dest.writeString(gameId);
        dest.writeString(description);
        dest.writeString(repairSteps);
        dest.writeLong(timestamp);
    }

    public final static Parcelable.Creator<RepairLog> CREATOR = new Parcelable.Creator<RepairLog>() {

        @Override
        public RepairLog createFromParcel(Parcel in) { return new RepairLog(in); }

        @Override
        public RepairLog[] newArray(int size) { return (new RepairLog[size]); }
    };

    // checks if two RepairLog objects are equal by comparing gameId which should be unique
    // as id is generated by firebase
    public boolean equals(RepairLog log) {
        return getId().equals(log.getId());
    }

    @Exclude
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGameId() {
        return gameId;
    }

    public void setGameId(String gameId) {
        this.gameId = gameId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getRepairSteps() {
        return repairSteps;
    }

    public void setRepairSteps(String repairSteps) {
        this.repairSteps = repairSteps;
    }

    public Long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(Long timestamp) {
        this.timestamp = timestamp;
    }
}
