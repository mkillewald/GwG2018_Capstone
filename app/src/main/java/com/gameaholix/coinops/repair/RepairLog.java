package com.gameaholix.coinops.repair;

import android.os.Parcel;
import android.os.Parcelable;

public class RepairLog implements Parcelable{
    private String id;
    private String name;

    public RepairLog() {
        // Default constructor required for calls to DataSnapshot.getValue()
    }

    public RepairLog(String name) {
        this.name = name;
    }

    private RepairLog(Parcel in) {
        this.id = in.readString();
        this.name = in.readString();
    }

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int i) {
        dest.writeString(id);
        dest.writeString(name);
    }

    public final static Parcelable.Creator<RepairLog> CREATOR = new Parcelable.Creator<RepairLog>() {

        @Override
        public RepairLog createFromParcel(Parcel in) { return new RepairLog(in); }

        @Override
        public RepairLog[] newArray(int size) { return (new RepairLog[size]); }
    };

    // checks if two RepairLog objects are equal by comparing gameId which should be unique
    // as id is generated by firebase
    public boolean equals(Object object) {
        if (object instanceof RepairLog) {
            RepairLog repairLog = (RepairLog) object;
            return getId() == repairLog.getId();
        } else {
            return false;
        }
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
